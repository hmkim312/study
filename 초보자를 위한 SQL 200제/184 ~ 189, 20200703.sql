-- 실제값과 예측확률 출력
SELECT EMP_ID, T.LEFT 실제값,
    PREDICTION (DT_MODEL3 USING *) 예측값,
    ROUND(PREDICTION_PROBABILITY (DT_MODEL3 USING *), 2) "예측한 확률"
        FROM HR_DATA_TEST T
